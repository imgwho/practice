/* 
题目描述
获取数字 num 二进制形式第 bit 位的值。注意：
1、bit 从 1 开始
2、返回 0 或 1
3、举例：2 的二进制为 10，第 1 位为 0，第 2 位为 1
示例1
输入

128, 8
输出

1

 */

 function parse(num, bit) {
  // a << b	将 a 的二进制形式向左移 b (< 32) 比特位，右边用0填充。
  // &1:按位与，如10000&00001，这样就能取到10000的最后一位数字
  // 后面的 & 运算，& 1 意味着只输出左操作数的最后一位，而 bit 从 1 开始算。
   return (num >> (bit - 1)) & 1
 }